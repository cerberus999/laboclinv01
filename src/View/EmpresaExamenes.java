    private void btnSelExamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelExamActionPerformed
        int selected = jList1.getSelectedIndex();
        String ANA_ID = exams.get(selected)[0];
        while(exams.get(selected)[0].contains(ANA_ID)){        
            selExams.add((String[]) exams.remove(selected));
        }
        fillJListSelector();
    }//GEN-LAST:event_btnSelExamActionPerformed

    private void btnUnselExamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUnselExamActionPerformed
        int selected = jList2.getSelectedIndex();
        String ANA_ID = selExams.get(selected)[0];
        while(!selExams.isEmpty() && selExams.get(selected)[0].contains(ANA_ID)){        
            exams.add(selExams.remove(selected));
        }
        fillJListSelector();
    }//GEN-LAST:event_btnUnselExamActionPerformed

