    private void fillJListSelector(){
        dlm = new DefaultListModel<>();
        selDlm = new DefaultListModel<>();
        for (String[] a : exams) {
            String fitter = "";
            if(a[0].length() > 1){
                fitter += "- ";
                if(a[0].length() > 4){
                    fitter += "- ";
                }
            }
            String aux = a[0] + "  " + fitter + a[1];
            dlm.addElement(aux);
        }
        jList1.setModel(dlm);
        jList1.repaint();
        //JOptionPane.showMessageDialog(this, dlm.getSize());
        
        for(String[] a: selExams){
            String fitter = "";
            if(a[0].length() > 1){
                fitter += "- ";
                if(a[0].length() > 4){
                    fitter += "- ";
                }
            }
            String aux = a[0] + "  " + fitter + a[1];
            selDlm.addElement(aux);
        }
        jList2.clearSelection();
        jList2.setModel(selDlm);
        jList2.repaint();
        //JOptionPane.showMessageDialog(this, selDlm.getSize());
    }
    
    private void getExams(){
        String query = "SELECT ANA_ID, ANA_Nombre, ANA_TipoDato FROM analito";
        SqlConector.conectar();
        try{
            ResultSet rs = SqlConector.executeQuery(query);
            while(rs.next()){
                String[] aux = new String[3];
                for(int i=0;i<3;i++){
                    aux[i] = rs.getString(i+1);
                }
                exams.add(aux);
            }
            
            SqlConector.closeConn();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this, e.getErrorCode()+": "+e.getMessage());
        }
        
    }

