    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Connection con = SqlConector.conectar();
        String query;
        PreparedStatement pstmt; 
        try {
            for(int j=0;j<pacientes.size();j++){
                Vector<String> a = pacientes.get(j);
                query = "INSERT INTO paciente values (0,?,?,?,?,?,?,?,0)";
                pstmt = con.prepareStatement(query);        
                pstmt.setString(1, a.get(0));
                pstmt.setString(2, a.get(1));
                pstmt.setString(3, a.get(2));
                pstmt.setString(4, a.get(3));
                pstmt.setString(5, a.get(4));
                pstmt.setString(6, a.get(5));
                pstmt.setString(7, a.get(6));
                pstmt.executeUpdate();
                pstmt.clearParameters();
                
                query = "SELECT PAC_ID FROM paciente ORDER BY PAC_ID DESC LIMIT 1";
                ResultSet rs = SqlConector.executeQuery(query);
                rs.next();
                int idPac = rs.getInt(1);
                
                
                query = "INSERT INTO hojaresultados VALUES(0,'" + textDate.getText() + 
                        "'," + idPac + ", 1)";
                pstmt = con.prepareStatement(query);
                pstmt.executeUpdate();
                
                query = "SELECT HR_ID FROM hojaresultados ORDER BY HR_ID DESC LIMIT 1";
                rs = SqlConector.executeQuery(query);
                rs.next();
                int idHR = rs.getInt(1);
                
                int indexX = 3,indexY = 0;
                String[] aux;
                
                int index = 0;
                for (int i=0;i<examenes.size();i++) {
                    aux = examenes.get(i);
                    if(!aux[2].equals("1")){
                        query = "INSERT INTO resultado VALUES(?,'"+aux[0]+"',"+idHR+")";
                        pstmt = con.prepareStatement(query);
                        pstmt.setString(1, (String)chargeTable.getValueAt(indexY+j, indexX+index));
                        pstmt.executeUpdate();
                        pstmt.clearParameters();
                        index++;
                    }
                }
                dispose();
                dispose();
                dispose();
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

